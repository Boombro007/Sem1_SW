#include<stdio.h>
int main()
{
    int n,i,j,k;
    for(int i=0;i<n;i++)
    {
        for(int j=i-1;i<n;i++)
        {
            printf(" ");
        }
        printf("\n");
        for(int k = 0;k<n;k++)
        {
            printf("%d",n);
        }
        printf("\n");
    }
    return 0;
}